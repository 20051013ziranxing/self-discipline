package com.example.myfragment.bean;

public class NewFunction {
    int newFunctionIcon;
    String newFunctionName;

    public NewFunction(int newFunctionIcon, String newFunctionName) {
        this.newFunctionIcon = newFunctionIcon;
        this.newFunctionName = newFunctionName;
    }

    public int getNewFunctionIcon() {
        return newFunctionIcon;
    }

    public String getNewFunctionName() {
        return newFunctionName;
    }
}
